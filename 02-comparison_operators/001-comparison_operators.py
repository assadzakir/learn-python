#  comparison operators

# Equal
print(2 == 2)  # True

print(2 == 0)  # False

# Not Equal

print(2 != 0)  # True

print(2 != 2)  # False

# print(2 <> 2)  # False (Python version 3.6 does not support <>, use != instead)

# Greater Than

print(2 > 1)  # True

print(2 > 4)  # False

# Less Than

print(2 < 1)  # False

print(2 < 4)  # True

# Greater Than Equal To

print(2 >= 2)  # True

print(2 >= 1)  # True

# Less Than Equal To

print(2 <= 2)  # True

print(2 <= 1)  # False